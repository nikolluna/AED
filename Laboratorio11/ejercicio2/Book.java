package ejercicio2;

class Book {
    String title;
    String author;
    int sales;
    int criticRating;
    String specialty;
    String comments;

    public Book(String title, String author, int sales, int criticRating, String specialty, String comments) {
        this.title = title;
        this.author = author;
        this.sales = sales;
        this.criticRating = criticRating;
        this.specialty = specialty;
        this.comments = comments;
    }
}

